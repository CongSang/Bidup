package com.charitysm.pojo.communicateObj;

import java.io.Serializable;

/**
 *
 * @author ADMIN
 */
public class NotifMessage implements Serializable{
    private int code;
    private Object data;

    public NotifMessage(int code, Object data) {
        this.code = code;
        this.data = data;
    }

    /**
     * @return the code
     */
    public int getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(int code) {
        this.code = code;
    }

    /**
     * @return the data
     */
    public Object getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(Object data) {
        this.data = data;
    }
    
}
